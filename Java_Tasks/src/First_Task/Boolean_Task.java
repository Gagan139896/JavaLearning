package First_Task;
public class Boolean_Task {
   static void myBool(boolean bool) {
        if (bool == true){
            System.out.println("Bool variable has '"+bool+"' value");
        }
        else {
            System.out.println("Bool variable has '"+bool+"' value");
        }
    }
    public static void main(String[] args) {
        Boolean_Task.myBool(true);
        
    }
}